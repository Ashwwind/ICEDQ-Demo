package automationTask;

public class PrimitiveDatatypeAndValues {

	// Declare primitive Data Type and prints its default values.

	// 1. Declare and initialized -- Byte Data Type.
	byte a;

	// 2. Declare and initialized -- Short Data Type.
	short b;

	// 3. Declare and initialized -- Int Data Type.
	int c;

	// 4. Declare and initialized -- Long Data Type.
	long d;

	// 5. Declare and initialized -- Short float Type.
	float e;

	// 6. Declare and initialized -- Double Data Type.
	double f;

	// 7. Declare and initialized -- Char Data Type
	char g;

	// 8. Declare and initialized -- Boolean Data Type
	boolean h;

	public static void main(String[] args) {

		PrimitiveDatatypeAndValues obj = new PrimitiveDatatypeAndValues();

		System.out.println("*********************************************");

		System.out.println("Declare primitive Data Type and prints its default values.");
		
		System.out.println("*********************************************");

		System.out.println("Prints the default value of Byte Data Type: " + obj.a);
		System.out.println("Prints the default value of Short Data Type: " + obj.b);
		System.out.println("Prints the default value of Int Data Type: " + obj.c);
		System.out.println("Prints the default value of long Data Type: " + obj.d);
		System.out.println("Prints the default value of Float Data Type: " + obj.e);
		System.out.println("Prints the default value of Double Data Type: " + obj.f);
		System.out.println("Prints the default value of Char Data Type: " + obj.g);
		System.out.println("Prints the default value of Boolean Data Type: " + obj.h);

		System.out.println("*********************************************");

	}

}
